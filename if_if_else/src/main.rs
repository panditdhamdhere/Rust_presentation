fn main() {
    println!("If - If - Else!");

    first();
}

fn first() {
    let num = 4;

    if num < 5 {
        println!("Less than 5");
    } else if num > 5 {
        println!("Greater than 5");
    } else {
        println!("Equal to 5");
    }
}

